package dottedFiguresWithMethods;

public class MyRectangle{

	int rectWidth;
	int rectHeight;
	char rectChar;
	int rectShiftFromLeft;
	MyRectangle(int width, int height, char symbol, int shiftFromLeft) {
		rectWidth = width;
		rectHeight = height;
		rectChar = symbol;
		rectShiftFromLeft = shiftFromLeft;
	}
	
}
